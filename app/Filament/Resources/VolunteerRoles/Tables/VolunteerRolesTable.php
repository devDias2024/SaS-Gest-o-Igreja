<?php

namespace App\Filament\Resources\VolunteerRoles\Tables;

use Filament\Actions\BulkActionGroup;
use Filament\Actions\DeleteBulkAction;
use Filament\Actions\EditAction;
use Filament\Tables\Columns\IconColumn;
use Filament\Tables\Columns\TextColumn;
use Filament\Tables\Table;

class VolunteerRolesTable
{
    public static function configure(Table $table): Table
    {
        return $table
            ->columns([
                TextColumn::make('name')->label('Nome')->searchable()->sortable(),
                TextColumn::make('default_slots')->label('Vagas'),
                IconColumn::make('rotates_automatically')->label('Rotacao')->boolean(),
                IconColumn::make('is_active')->label('Ativa')->boolean(),
                TextColumn::make('assignments_count')->label('Escalas')->counts('assignments')->sortable(),
            ])
            ->filters([
                //
            ])
            ->recordActions([
                EditAction::make(),
            ])
            ->toolbarActions([
                BulkActionGroup::make([
                    DeleteBulkAction::make(),
                ]),
            ]);
    }
}
