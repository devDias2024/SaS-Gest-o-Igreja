<?php

namespace App\Filament\Resources\EventLocations\Tables;

use Filament\Actions\BulkActionGroup;
use Filament\Actions\DeleteBulkAction;
use Filament\Actions\EditAction;
use Filament\Tables\Columns\IconColumn;
use Filament\Tables\Columns\TextColumn;
use Filament\Tables\Table;

class EventLocationsTable
{
    public static function configure(Table $table): Table
    {
        return $table
            ->columns([
                TextColumn::make('name')->label('Nome')->searchable()->sortable(),
                TextColumn::make('capacity')->label('Capacidade')->sortable(),
                TextColumn::make('geofence_radius_meters')->label('Raio')->suffix(' m'),
                IconColumn::make('is_active')->label('Ativo')->boolean(),
                TextColumn::make('events_count')->label('Eventos')->counts('events')->sortable(),
            ])
            ->filters([
                //
            ])
            ->recordActions([
                EditAction::make(),
            ])
            ->toolbarActions([
                BulkActionGroup::make([
                    DeleteBulkAction::make(),
                ]),
            ]);
    }
}
